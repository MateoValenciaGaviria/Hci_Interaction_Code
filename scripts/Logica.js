class Logica {

    constructor(app) {
        this.app = app;
        this.precio;

        this.valor;

        switch (precio) {
            case Enero:
                valor = 50;
                break;

            case Febrero:
                valor = 0;
                break;

            case Marzo:
                valor = 0;
                break;

            case Abril:
                valor = 0;
                break;

            case Mayo:
                valor = 0;
                break;

            case Junio:
                valor = 0;
                break;

            case Julio:
                valor = 0;
                break;

            case Agosto:
                valor = 0;
                break;

            case Septiembre:
                valor = 0;
                break;
            default:
                break;
        }
    }



    mouse() {

    }


    pintar() {
        this.app.background(255, 200, 200);
    }
}

